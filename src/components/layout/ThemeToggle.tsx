export function ThemeToggle() {
  const { theme, toggle } = useTheme();
  const isDark = theme === "dark";

  return (
    <button
      type="button"
      onClick={toggle}
      aria-label={isDark ? "Switch to light mode" : "Switch to dark mode"}
      aria-pressed={isDark}
      className="grid h-8 w-8 place-items-center rounded-full border border-border bg-bg-secondary text-[color:var(--accent-primary)] transition-colors duration-300 hover:border-[color:var(--accent-primary)] hover:bg-bg-tertiary focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[color:var(--accent-primary)] focus-visible:ring-offset-2 focus-visible:ring-offset-[color:var(--bg-primary)]"
    >
      {isDark ? <MoonIcon /> : <SunIcon />}
    </button>
  );
}
